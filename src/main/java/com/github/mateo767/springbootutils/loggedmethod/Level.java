package com.github.mateo767.springbootutils.loggedmethod;

/**
 * The log levels dictionary.
 */
public enum Level {
    /**
     * Print logs at the default level. If unchanged, it is the same as INFO.
     */
    DEFAULT,
    /**
     * Print logs at the ERROR level.
     */
    ERROR,
    /**
     * Print logs at the WARN level.
     */
    WARN,
    /**
     * Print logs at the INFO level.
     */
    INFO,
    /**
     * Print logs at the DEBUG level.
     */
    DEBUG,
    /**
     * Print logs at the TRACE level.
     */
    TRACE
}
